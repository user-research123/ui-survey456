def parse_posts(text):
    """解析帖子文本,提取用户名、时间和评论数"""
    posts = []
    
    # 分割文本为行
    lines = text.strip().split('\n')
    
    i = 0
    while i < len(lines):
        # 跳过空行
        if not lines[i].strip():
            i += 1
            continue
        
        # 第一行是用户名
        username = lines[i].strip()
        i += 1
        
        # 第二行是时间
        if i >= len(lines):
            break
        time_str = lines[i].strip()
        i += 1
        
        # 第三行是"来自 洛克王国世界交易超话"(可能包含"最后评论")
        if i >= len(lines):
            break
        source_line = lines[i].strip()
        if "来自" not in source_line:
            # 如果不是来源行,可能是帖子内容的一部分
            i -= 1
        else:
            i += 1
        
        # 接下来是帖子内容,直到遇到评论数行
        content_lines = []
        while i < len(lines):
            line = lines[i].strip()
            # 检查是否是评论数行
            if line.startswith(''):
                # 这是评论数行
                comment_line = line
                i += 1
                
                # 提取评论数
                comment_count = 0
                if '评论' in comment_line and '(' not in comment_line:
                    # 格式: 评论(表示0条评论)
                    comment_count = 0
                elif '评论' in comment_line and '(' in comment_line:
                    # 格式: 评论(0条)
                    match = re.search(r'\((\d+)条\)', comment_line)
                    if match:
                        comment_count = int(match.group(1))
                elif '' in comment_line:
                    # 格式:  [数字]
                    match = re.search(r'\s*(\d+)', comment_line)
                    if match:
                        comment_count = int(match.group(1))
                    else:
                        comment_count = 0
                
                # 组合帖子内容
                content = ' '.join(content_lines).strip()
                # 移除开头的标签
                if content.startswith(''):
                    content = content[1:].strip()
                
                posts.append({
                    'username': username,
                    'time': time_str,
                    'content': content,
                    'comment_count': comment_count
                })
                break
            else:
                content_lines.append(line)
                i += 1
        
        # 如果没有找到评论数行,跳出循环
        if i >= len(lines):
            break
    
    return posts

def main():
    # 解析第1页的帖子
    page1_posts = parse_posts(page1_text)
    
    print(f"第1页帖子数: {len(page1_posts)}")
    
    # 限制为前80个帖子(如果不足80个,则使用所有帖子)
    final_posts = page1_posts[:80]
    
    print(f"最终帖子数: {len(final_posts)}")
    
    # 保存为CSV
    with open('/Users/jiewen/.real/users/user-777da3823a68e71779b041c8e7807df0/workspace/微博超话帖子数据_最终版.csv', 'w', newline='', encoding='utf-8-sig') as f:
        writer = csv.writer(f)
        writer.writerow(['序号', '用户名', '时间', '帖子内容', '评论数'])
        for idx, post in enumerate(final_posts, 1):
            writer.writerow([idx, post['username'], post['time'], post['content'], post['comment_count']])
    
    # 保存为JSON
    with open('/Users/jiewen/.real/users/user-777da3823a68e71779b041c8e7807df0/workspace/微博超话帖子数据_最终版.json', 'w', encoding='utf-8') as f:
        json.dump(final_posts, f, ensure_ascii=False, indent=2)
    
    # 保存为Excel
    wb = Workbook()
    ws = wb.active
    ws.title = "微博超话帖子数据"
    ws.append(['序号', '用户名', '时间', '帖子内容', '评论数'])
    for idx, post in enumerate(final_posts, 1):
        ws.append([idx, post['username'], post['time'], post['content'], post['comment_count']])
    wb.save('/Users/jiewen/.real/users/user-777da3823a68e71779b041c8e7807df0/workspace/微博超话帖子数据_最终版.xlsx')
    
    print("数据已保存到:")
    print("- 微博超话帖子数据_最终版.csv")
